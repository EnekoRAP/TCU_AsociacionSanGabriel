		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_BENEFICIARIOS.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_PROFESOR RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_PROFESORES.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_NIVEL RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_NIVELES.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_ASIGNATURA RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_ASIGNATURAS.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_MATERIAL RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_MATERIALES.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_EVENTO RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_EVENTOS.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_BITACORA RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_BITACORAS.NEXTVAL INTO V_NEXT FROM DUAL;
		RETURN V_NEXT;
	END;
	
	FUNCTION NEXT_ERROR RETURN NUMBER IS
		V_NEXT NUMBER;
	BEGIN
		SELECT SEQ_AUDITORIA_ERRORES.NEXTVAL INTO V_NEXT FROM DUAL;
