CREATE TABLE TBL_PROFESORES (
	ID_PROFESOR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	IDENTIFICACION VARCHAR2(15) NOT NULL,
	NOMBRE VARCHAR2(50) NOT NULL,
	APELLIDOS VARCHAR2(50) NOT NULL,
	ESPECIALIDAD VARCHAR2(100) NOT NULL,
	CANTIDAD_TITULOS NUMBER NOT NULL,
	CORREO VARCHAR2(100) NOT NULL,
	RUTA_IMAGEN VARCHAR2(1024)
);
