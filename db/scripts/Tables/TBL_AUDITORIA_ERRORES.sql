CREATE TABLE TBL_AUDITORIA_ERRORES (
	ID_ERROR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	FECHA_HORA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	MENSAJE VARCHAR2(1024) NOT NULL,
	ORIGEN VARCHAR2(1024) NOT NULL,
	ID_USUARIO NUMBER,
	CONSTRAINT FK_ERROR_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES TBL_USUARIOS(ID_USUARIO)
);
