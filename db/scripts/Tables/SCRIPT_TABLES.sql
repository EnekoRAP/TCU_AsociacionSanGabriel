-------------------------------------------------------------------------------------------------------------
-------------------------------------------- CREACION DE TABLAS ---------------------------------------------
-------------------------------------------------------------------------------------------------------------

-- TABLA DE ROLES
CREATE TABLE TBL_ROLES (
	ID_ROL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NOMBRE VARCHAR2(50) NOT NULL
);

-- TABLA DE USUARIOS
CREATE TABLE TBL_USUARIOS (
	ID_USUARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	IDENTIFICACION VARCHAR2(15) NOT NULL,
	NOMBRE VARCHAR2(50) NOT NULL,
	APELLIDOS VARCHAR2(50) NOT NULL,
	CORREO VARCHAR2(100) NOT NULL,
	CONTRASENNA VARCHAR2(255) NOT NULL,
	RUTA_IMAGEN VARCHAR2(1024),
	ESTADO NUMBER(1),
	ID_ROL NUMBER,
	CONSTRAINT FK_USUARIO_ROL FOREIGN KEY (ID_ROL) REFERENCES TBL_ROLES(ID_ROL)	
);

-- TABLA DE BENEFICIARIOS
CREATE TABLE TBL_BENEFICIARIOS (
	ID_BENEFICIARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	IDENTIFICACION VARCHAR2(15) NOT NULL,
	NOMBRE VARCHAR2(50) NOT NULL,
	APELLIDOS VARCHAR2(50) NOT NULL,
	FECHA_NACIMIENTO DATE NOT NULL,
	GENERO VARCHAR2(10) NOT NULL,
	DIRECCION VARCHAR2(200) NOT NULL,
	CONTACTO NUMBER,
	CONDICION_ESPECIAL VARCHAR2(1024),
	PADECIMIENTOS VARCHAR2(100),
	ALERGIAS VARCHAR2(100),
	RUTA_IMAGEN VARCHAR2(1024),
	ID_NIVEL NUMBER,
	CONSTRAINT FK_BENEFICIARIO_NIVEL FOREIGN KEY (ID_NIVEL) REFERENCES TBL_NIVELES(ID_NIVEL)
);

-- TABLA DE PROFESORES
CREATE TABLE TBL_PROFESORES (
	ID_PROFESOR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	IDENTIFICACION VARCHAR2(15) NOT NULL,
	NOMBRE VARCHAR2(50) NOT NULL,
	APELLIDOS VARCHAR2(50) NOT NULL,
	ESPECIALIDAD VARCHAR2(100) NOT NULL,
	CANTIDAD_TITULOS NUMBER NOT NULL,
	CORREO VARCHAR2(100) NOT NULL,
	RUTA_IMAGEN VARCHAR2(1024)
);

-- TABLA DE NIVELES
CREATE TABLE TBL_NIVELES (
	ID_NIVEL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	CODIGO VARCHAR2(10) NOT NULL,
	NOMBRE VARCHAR2(50) NOT NULL
);

-- TABLA DE ASIGNATURAS
CREATE TABLE TBL_ASIGNATURAS (
	ID_ASIGNATURA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NOMBRE VARCHAR2(50) NOT NULL,
	ID_PROFESOR NUMBER,
	ID_NIVEL NUMBER,
	CONSTRAINT FK_ASIGNATURA_PROFESOR FOREIGN KEY (ID_PROFESOR) REFERENCES TBL_PROFESORES(ID_PROFESOR),
	CONSTRAINT FK_ASIGNATURA_NIVEL FOREIGN KEY (ID_NIVEL) REFERENCES TBL_NIVELES(ID_NIVEL)
);

-- TABLA DE MATERIALES
CREATE TABLE TBL_MATERIALES (
	ID_MATERIAL NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NOMBRE VARCHAR2(50) NOT NULL,
	TIPO VARCHAR2(50) NOT NULL,
	CANTIDAD NUMBER NOT NULL,
	ESTADO NUMBER(1),
	ID_ASIGNATURA NUMBER,
	CONSTRAINT FK_MATERIAL_ASIGNATURA FOREIGN KEY (ID_ASIGNATURA) REFERENCES TBL_ASIGNATURAS(ID_ASIGNATURA)
);

-- TABLA DE EVENTOS
CREATE TABLE TBL_EVENTOS (
	ID_EVENTO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NOMBRE VARCHAR2(50) NOT NULL,
	FECHA_INICIO DATE NOT NULL,
	FECHA_FIN DATE NOT NULL,
	DESCRIPCION VARCHAR2(1024) NOT NULL,
	RUTA_IMAGEN VARCHAR2(1024)
);

-- TABLA DE PARTICIPACION DE EVENTOS
CREATE TABLE TBL_PARTICIPACION_EVENTOS (
	ID_EVENTO NUMBER,
	ID_BENEFICIARIO NUMBER,
	CONSTRAINT PK_PARTICIPACION PRIMARY KEY (ID_EVENTO, ID_BENEFICIARIO),
	CONSTRAINT FK_PARTICIPACION_EVENTO FOREIGN KEY (ID_EVENTO) REFERENCES TBL_EVENTOS(ID_EVENTO),
	CONSTRAINT FK_PARTICIPACION_BENEFICIARIO FOREIGN KEY (ID_BENEFICIARIO) REFERENCES TBL_BENEFICIARIOS(ID_BENEFICIARIO)
);

-- TABLA DE BITACORAS
CREATE TABLE TBL_BITACORAS (
	ID_BITACORA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	FECHA_HORA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	ACCION VARCHAR(1024) NOT NULL,
	ID_USUARIO NUMBER,
	CONSTRAINT FK_BITACORA_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES TBL_USUARIOS(ID_USUARIO)
);

-- TABLA DE AUDITORIA DE ERRORES
CREATE TABLE TBL_AUDITORIA_ERRORES (
	ID_ERROR NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	FECHA_HORA TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	MENSAJE VARCHAR2(1024) NOT NULL,
	ORIGEN VARCHAR2(1024) NOT NULL,
	ID_USUARIO NUMBER,
	CONSTRAINT FK_ERROR_USUARIO FOREIGN KEY (ID_USUARIO) REFERENCES TBL_USUARIOS(ID_USUARIO)
);
