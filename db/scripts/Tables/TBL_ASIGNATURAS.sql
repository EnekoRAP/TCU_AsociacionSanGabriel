CREATE TABLE TBL_ASIGNATURAS (
	ID_ASIGNATURA NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	NOMBRE VARCHAR2(50) NOT NULL,
	ID_PROFESOR NUMBER,
	ID_NIVEL NUMBER,
	CONSTRAINT FK_ASIGNATURA_PROFESOR FOREIGN KEY (ID_PROFESOR) REFERENCES TBL_PROFESORES(ID_PROFESOR),
	CONSTRAINT FK_ASIGNATURA_NIVEL FOREIGN KEY (ID_NIVEL) REFERENCES TBL_NIVELES(ID_NIVEL)
);
