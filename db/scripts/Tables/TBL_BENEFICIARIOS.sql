CREATE TABLE TBL_BENEFICIARIOS (
	ID_BENEFICIARIO NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	IDENTIFICACION VARCHAR2(15) NOT NULL,
	NOMBRE VARCHAR2(50) NOT NULL,
	APELLIDOS VARCHAR2(50) NOT NULL,
	FECHA_NACIMIENTO DATE NOT NULL,
	GENERO VARCHAR2(10) NOT NULL,
	DIRECCION VARCHAR2(200) NOT NULL,
	CONTACTO NUMBER,
	CONDICION_ESPECIAL VARCHAR2(1024),
	PADECIMIENTOS VARCHAR2(100),
	ALERGIAS VARCHAR2(100),
	RUTA_IMAGEN VARCHAR2(1024),
	ID_NIVEL NUMBER,
	CONSTRAINT FK_BENEFICIARIO_NIVEL FOREIGN KEY (ID_NIVEL) REFERENCES TBL_NIVELES(ID_NIVEL)
);
