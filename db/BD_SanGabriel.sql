-- FUNCIÓN PARA OBTENER NOMBRE COMPLETO DE USUARIO POR ID
CREATE OR REPLACE FUNCTION FN_NOMBRE_COMPLETO_USUARIO (
	P_ID_USUARIO IN NUMBER
) RETURN VARCHAR2 IS
	V_QUERY VARCHAR2(1000);
	V_NOMBRE_COMPLETO VARCHAR2(150);
BEGIN
	V_QUERY := 'SELECT NOMBRE || '' '' || APELLIDOS FROM TBL_USUARIOS WHERE ID_USUARIO = :1';
	
	EXECUTE IMMEDIATE V_QUERY INTO V_NOMBRE_COMPLETO USING P_ID_USUARIO;
	
	RETURN V_NOMBRE_COMPLETO;
END;

-- FUNCIÓN PARA CONVERTIR ESTADO DE UN USUARIO
CREATE OR REPLACE FUNCTION FN_ESTADO_TEXTO_USUARIO (
	P_ID_USUARIO IN NUMBER
) RETURN VARCHAR2 IS
	V_ESTADO NUMBER;
BEGIN
	SELECT ESTADO INTO V_ESTADO
	FROM TBL_USUARIOS
	WHERE ID_USUARIO = P_ID_USUARIO;
	
	RETURN CASE V_ESTADO WHEN 1 THEN 'Activo' ELSE 'Inactivo'
	END;
END;

-- FUNCIÓN PARA CONTAR USUARIOS POR ROL
CREATE OR REPLACE FUNCTION FN_TOTAL_USUARIOS_POR_ROL (
	P_ID_ROL IN NUMBER
) RETURN NUMBER IS
	V_CANTIDAD NUMBER;
BEGIN
	SELECT COUNT(*) INTO V_CANTIDAD
	FROM TBL_USUARIOS
	WHERE ID_ROL = P_ID_ROL;
	
	RETURN V_CANTIDAD;
END;

-- FUNCIÓN PARA VERIFICAR EXISTENCIA DE UN BENEFICIARIO SEGÚN CÉDULA
CREATE OR REPLACE FUNCTION FN_EXISTE_BENEFICIARIO (
	P_IDENTIFICACION IN VARCHAR2
) RETURN NUMBER IS
	V_EXISTE NUMBER;
BEGIN
	SELECT COUNT(*) INTO V_EXISTE
	FROM TBL_BENEFICIARIOS
	WHERE IDENTIFICACION = P_IDENTIFICACION;
	
	RETURN CASE WHEN V_EXISTE > 0 THEN 1 ELSE 0
	END;
END;

-- FUNCIÓN PARA CONTAR BENEFICIARIOS ASIGNADOS A UN NIVEL
CREATE OR REPLACE FUNCTION FN_CONTAR_BENEFICIARIOS_NIVEL (
	P_ID_NIVEL IN NUMBER
) RETURN NUMBER IS
	V_TOTAL NUMBER;
BEGIN
	SELECT COUNT(*) INTO V_TOTAL
	FROM TBL_BENEFICIARIOS
	WHERE ID_NIVEL = P_ID_NIVEL;
	
	RETURN V_TOTAL;
END;

-- FUNCIÓN PARA OBTENER EDAD DE BENEFICIARIO
CREATE OR REPLACE FUNCTION FN_OBTENER_EDAD_BENEFICIARIO (
	P_ID_BENEFICIARIO IN NUMBER
) RETURN NUMBER IS
	V_FECHA_NAC DATE;
	V_EDAD NUMBER;
BEGIN
	SELECT FECHA_NACIMIENTO INTO V_FECHA_NAC
	FROM TBL_BENEFICIARIOS
	WHERE ID_BENEFICIARIO = P_ID_BENEFICIARIO;
	
	V_EDAD := TRUNC(MONTHS_BETWEEN(SYSDATE, V_FECHA_NAC) / 12);
	RETURN V_EDAD;
END;

-- FUNCIÓN PARA CALCULAR CANTIDAD TOTAL DE MATERIALES ACTIVOS POR ASIGNATURA
CREATE OR REPLACE FUNCTION FN_MATERIALES_DISPONIBLES_ASIGNATURA (
	P_ID_ASIGNATURA IN NUMBER
) RETURN NUMBER IS
	V_CANTIDAD NUMBER;
	V_SQL VARCHAR(1000);
BEGIN
	V_SQL := 'SELECT COUNT(*) FROM TBL_MATERIALES WHERE ID_ASIGNATURA = :1 AND ESTADO = 1';
	
	EXECUTE IMMEDIATE V_SQL INTO V_CANTIDAD USING P_ID_ASIGNATURA;
	
	RETURN V_CANTIDAD;
END;

-- FUNCIÓN PARA CANTIDAD DE BENEFICIARIOS PARTICIPANDO EN UN EVENTO
CREATE OR REPLACE FUNCTION FN_CANTIDAD_PARTICIPACIONES_EVENTO (
	P_ID_EVENTO IN NUMBER
) RETURN NUMBER IS
	V_TOTAL NUMBER;
BEGIN
	SELECT COUNT(*) INTO V_TOTAL
	FROM TBL_PARTICIPACION_EVENTOS
	WHERE ID_EVENTO = P_ID_EVENTO;
	
	RETURN V_TOTAL;
END;

-------------------------------------------- CREACIóN DE TRIGGERS --------------------------------------------

-- TRIGGER PARA VALIDAR QUE LOS BENEFICIARIOS TENGAN UNA EDAD MÍNIMA DE 0 AÑOS
CREATE OR REPLACE TRIGGER TRG_VALIDA_EDAD_BENEFICIARIO
BEFORE INSERT OR UPDATE ON TBL_BENEFICIARIOS
FOR EACH ROW
DECLARE	
	V_EDAD NUMBER;
BEGIN
	V_EDAD := TRUNC(MONTHS_BETWEEN(SYSDATE, :NEW.FECHA_NACIMIENTO) / 12);
	
	IF V_EDAD < 0 THEN
		RAISE_APPLICATION_ERROR(-20002, 'La fecha de nacimiento no puede estar en el futuro. ');
	END IF;
END;

-- TRIGGER PARA INSERTAR MENSAJE EN LA BITÁCORA CUANDO UN BENEFICIARIO ES MODIFICADO
CREATE OR REPLACE TRIGGER TRG_LOG_CAMBIOS_BENEFICIARIO
AFTER UPDATE ON TBL_BENEFICIARIOS
FOR EACH ROW
BEGIN
	INSERT INTO TBL_BITACORAS(ACCION, ID_USUARIO)
	VALUES ('Modificación de beneficiario: ' || :OLD.NOMBRE || ' ' || :OLD.APELLIDOS, NULL);
END;

-- TRIGGER PARA ASIGNAR ESTADO DEL MATERIAL COMO ACTIVO SI NO SE ESPECIFICA
CREATE OR REPLACE TRIGGER TRG_ESTADO_MATERIAL
BEFORE INSERT ON TBL_MATERIALES
FOR EACH ROW
BEGIN
	IF :NEW.ESTADO IS NULL THEN
		:NEW.ESTADO := 1;
	END IF;
END;

-- TRIGGER PARA INSERTAR EVENTOS CON FECHA DE FINALIZACIÓN IGUAL A LA DE INICIO
CREATE OR REPLACE TRIGGER TRG_VALIDAR_FECHA_EVENTO
BEFORE INSERT OR UPDATE ON TBL_EVENTOS
FOR EACH ROW
BEGIN 
	IF :NEW.FECHA_FIN < :NEW.FECHA_INICIO THEN
		RAISE_APPLICATION_ERROR(-20001, 'La fecha de finalización no puede ser anterior a la fecha de inicio. ');
	END IF;
END;

-- TRIGGER PARA NO ELIMINAR EVENTOS SI YA TIENEN BENEFICIARIOS INSCRITOS
CREATE OR REPLACE TRIGGER TRG_NO_BORRAR_EVENTO_CON_PARTICIPANTES
BEFORE DELETE ON TBL_EVENTOS
FOR EACH ROW
DECLARE
	V_CANTIDAD NUMBER;
BEGIN
	SELECT COUNT(*) INTO V_CANTIDAD
	FROM TBL_PARTICIPACION_EVENTOS
	WHERE ID_EVENTO = :OLD.ID_EVENTO;
	
	IF V_CANTIDAD > 0 THEN
		RAISE_APPLICATION_ERROR(-20003, 'No se puede eliminar un evento con participantes registrados. ');
	END IF;
END;

-- TRIGGER PARA REGISTRAR BITÁCORA CUANDO UN USUARIO ES INSERTADO
CREATE OR REPLACE TRIGGER TRG_AUDIT_USUARIOS
AFTER INSERT OR UPDATE OR DELETE ON TBL_USUARIOS
FOR EACH ROW
BEGIN
	INSERT INTO TBL_BITACORAS (ACCION, ID_USUARIO)
	VALUES (
		CASE
			WHEN INSERTING THEN 'INSERT'
			WHEN UPDATING THEN 'UPDATE'
			WHEN DELETING THEN 'DELETE'
		END,
		NVL(:NEW.ID_USUARIO, :OLD.ID_USUARIO)
	);
END;

-- TRIGGER PARA AUTOREGISTRAR UN ERROR EN ALGUNA TABLA CRÍTICA
CREATE OR REPLACE TRIGGER TRG_AUTOREGISTRO_BITACORA_ERRORES
AFTER INSERT ON TBL_AUDITORIA_ERRORES
FOR EACH ROW
BEGIN
	INSERT INTO TBL_BITACORAS (ACCION, ID_USUARIO)
	VALUES (
		'Se registró un error: ' || :NEW.MENSAJE || ' | Origen: ' || :NEW.ORIGEN,
		:NEW.ID_USUARIO
	);
END;

-- TRIGGER PARA IMRPIMIR MENSAJE CUANDO SE INSERTA UN NUEVO ERROR
CREATE OR REPLACE TRIGGER TRG_AUDIT_ERRORES
AFTER INSERT ON TBL_AUDITORIA_ERRORES
FOR EACH ROW
BEGIN
	DBMS_OUTPUT.PUT_LINE('Error registrado: ' || :NEW.MENSAJE || ' | Origen: ' || :NEW.ORIGEN);
END;

